/*ORACLE 12c and above*/
CREATE TABLE SECRET_IV 
(
  IV_ID NUMBER(10, 0) GENERATED ALWAYS AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999999999999999999999 MINVALUE 1 CACHE 20 NOT NULL 
, IV_RAW RAW(16) 
, CONSTRAINT SECRET_IV_PK PRIMARY KEY 
  (
    IV_ID 
  )
) 



/*For older versions */
CREATE TABLE SECRET_IV 
(
  IV_ID NUMBER(10, 0) NOT NULL 
, IV_RAW RAW(16) 
, CONSTRAINT SECRET_IV_PK PRIMARY KEY 
  (
    IV_ID 
  )
) ;

CREATE SEQUENCE SECRET_IV_SEQ START WITH 1;

CREATE OR REPLACE TRIGGER TRG_SECRET_IV 
BEFORE INSERT ON SECRET_IV 
FOR EACH ROW
BEGIN
  :new.IV_ID := SECRET_IV_SEQ.nextval;
END;